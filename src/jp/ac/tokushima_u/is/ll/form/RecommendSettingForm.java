package jp.ac.tokushima_u.is.ll.form;

public class RecommendSettingForm {
	private String nickname;
	private String natilanguage;
	private String sex;
	private String jlpt;
	private String month;
	private String major;
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	public String getNatilanguage() {
		return natilanguage;
	}
	public void setNatilanguage(String natilanguage) {
		this.natilanguage = natilanguage;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getJlpt() {
		return jlpt;
	}
	public void setJlpt(String jlpt) {
		this.jlpt = jlpt;
	}
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	public String getMajor() {
		return major;
	}
	public void setMajor(String major) {
		this.major = major;
	}
}
